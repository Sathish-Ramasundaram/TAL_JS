// What is an Object?
// An object groups related data using named keys.

const user = {
    name: "Sathish",
    age: 31,
    "job title": "Software Developer",
    isAdmin: false
};

// Accessing Object Properties:

// Dot Notation (Most Common)

console.log(user.name); // Sathish

// Bracket notation

console.log(user["job title"]);